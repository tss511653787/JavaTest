package MianShiBaoDian;

public class FuZhiCaoZuo {
	/*
	 * java中=(赋值操作)的返回值
	 */
	public static void main(String[] args) {
		boolean flag = false;
		if (flag = true) {
			System.out.println("yes");
		}
		Boolean a = false;
		if (a = false) {
			System.out.println("yes?");
		} else {
			System.out.println("No");
		}

		int num = 10;
		System.out.println(num = 50);
	}

}
